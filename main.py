def get_snd_mode(target_date):
    sorted_dates = sorted(SND_DATA.keys(), reverse=True)
    target_str = target_date.strftime("%y.%m.%d")
    for d in sorted_dates:
        if d <= target_str:
            return SND_DATA[d]
    return "N"

