def run_3q_engine(df, seed, fee, comp_p, comp_l, cycle_d):
    cash = seed
    operating_seed = seed
    history = []
    accumulated_profit = 0
    update_counter = 0

    # positions: Î≥¥Ïú† Ï§ëÏù∏ Ìã∞Ïñ¥ Î¶¨Ïä§Ìä∏
    # Í∞Å ÏöîÏÜå: {Îß§ÏàòÏùº, Îß§ÏàòÍ∞Ä, ÏàòÎüâ, Î™©ÌëúÍ∞Ä, ÎßåÍ∏∞Ïùº(MOCÎÇ†Ïßú), Î™®Îìú}
    positions = []

    # Ïù¥ÎØ∏ÏßÄ ÌååÎùºÎØ∏ÌÑ∞
    PARAMS = {
        "S": {"buy": 0.04,  "sell": 0.037, "moc": 17},
        "D": {"buy": 0.006, "sell": 0.010, "moc": 25},
        "N": {"buy": 0.05,  "sell": 0.030, "moc": 2}
    }

    for i in range(1, len(df)):
        current_date = df.index[i]
        prev_close = float(df['Close'].iloc[i-1])
        
        # ÏïºÌõÑ Îç∞Ïù¥ÌÑ∞Îäî ÏàòÏ†ïÏ£ºÍ∞Ä ÎØ∏Î∞òÏòÅÏãú Open/High/Low/CloseÍ∞Ä Î™ÖÌôïÌï®
        curr_open = float(df['Open'].iloc[i])
        curr_low = float(df['Low'].iloc[i])
        curr_high = float(df['High'].iloc[i])
        curr_close = float(df['Close'].iloc[i])

        mode = get_snd_mode(current_date)
        p = PARAMS.get(mode, PARAMS["N"])

        # --- [1] ÏãúÎìú Í∞±Ïã† (Î≥µÎ¶¨) ---
        update_counter += 1
        if update_counter >= cycle_d:
            if accumulated_profit > 0:
                operating_seed += (accumulated_profit * comp_p)
            else:
                operating_seed += (accumulated_profit * comp_l)
            accumulated_profit = 0
            update_counter = 0

        # --- [2] Îß§ÎèÑ Ï≤¥ÌÅ¨ (Î≥¥Ïú† Î¨ºÎüâ Í∞úÎ≥Ñ ÌôïÏù∏) ---
        # ÏóëÏÖÄÏ≤òÎüº Í∞Å Î¨ºÎüâÏù¥ ÎèÖÎ¶ΩÏ†ÅÏúºÎ°ú Î™©ÌëúÍ∞Ä/ÎßåÍ∏∞Î•º Í∞ÄÏßê
        next_positions = []
        
        for pos in positions:
            is_sold = False
            
            # (A) Î™©ÌëúÍ∞Ä ÏùµÏ†à (Îß§ÏàòÎêú Í∞ÄÍ≤© Í∏∞Ï§Ä + Î™©Ìëú%)
            # ÏóëÏÖÄ: 'Îß§ÎèÑÎ™©Ìëú'Í∞Ä Í≥†Ï†ïÎêòÏñ¥ ÏûàÏùå.
            if curr_high >= pos['target_price']:
                # Î≥¥ÏàòÏ†Å Í≥ÑÏÇ∞: Î™©ÌëúÍ∞ÄÍ∞Ä ÏãúÍ∞ÄÎ≥¥Îã§ ÎÇÆÏúºÎ©¥ ÏãúÍ∞Ä Îß§ÎèÑ, ÏïÑÎãàÎ©¥ Î™©ÌëúÍ∞Ä Îß§ÎèÑ
                sell_price = pos['target_price']
                if curr_open > sell_price: sell_price = curr_open 
                
                sell_val = pos['qty'] * sell_price
                profit = sell_val - (pos['qty'] * pos['buy_price'])
                
                cash += sell_val * (1 - fee)
                accumulated_profit += profit
                is_sold = True
            
            # (B) MOC ÎßåÍ∏∞ Ï≤≠ÏÇ∞
            # ÏóëÏÖÄ: MOC ÎÇ†ÏßúÍ∞Ä ÎêòÎ©¥ Ï¢ÖÍ∞Ä Ï≤≠ÏÇ∞
            # Î≥¥Ïú†ÏùºÏàò = (Ïò§Îäò - Îß§ÏàòÏùº).days
            elif not is_sold:
                held_days = (current_date - pos['buy_date']).days
                if held_days >= pos['moc_limit_days']:
                    sell_val = pos['qty'] * curr_close
                    profit = sell_val - (pos['qty'] * pos['buy_price'])
                    
                    cash += sell_val * (1 - fee)
                    accumulated_profit += profit
                    is_sold = True
            
            if not is_sold:
                next_positions.append(pos)
        
        positions = next_positions

        # --- [3] Îß§Ïàò Ï≤¥ÌÅ¨ (ÎèÖÎ¶Ω Ìã∞Ïñ¥ ÏßÑÏûÖ) ---
        # ÏóëÏÖÄ: Îß§Ïùº 'Ï†ÑÏùºÏ¢ÖÍ∞Ä' Í∏∞Ï§Ä ÌïòÎùΩÌè≠ Í≥ÑÏÇ∞ÌïòÏó¨ Ï£ºÎ¨∏
        target_buy_price = prev_close * (1 - p["buy"])
        
        # ÌòÑÏû¨ Î≥¥Ïú†Ï§ëÏù∏ Ìã∞Ïñ¥ Ïàò = Î¶¨Ïä§Ìä∏ Í∏∏Ïù¥
        current_tier_index = len(positions) + 1

        # Ï£ºÎ¨∏ Í∞ÄÍ≤©Î≥¥Îã§ Ï†ÄÍ∞ÄÍ∞Ä ÎÇÆÏúºÎ©¥ Ï≤¥Í≤∞ (Limit Order)
        if curr_low <= target_buy_price and current_tier_index <= 8:
            
            # Ìã∞Ïñ¥Î≥Ñ Í∞ÄÏ§ëÏπò (ÏóëÏÖÄ Î°úÏßÅ Ï∂îÏ†ï Î∞è ÏùºÎ∞òÏ†Å 3Q Î£∞)
            # 1~4, 7Ìã∞Ïñ¥: 1Î∞∞Ïàò / 5,6,8Ìã∞Ïñ¥: Í∞ÄÏ§ëÏπò
            if current_tier_index in [1, 2, 3, 4, 7]: unit_multiplier = 1.0
            elif current_tier_index == 5: unit_multiplier = 3.6
            elif current_tier_index == 6: unit_multiplier = 3.0
            elif current_tier_index == 8: unit_multiplier = 4.0
            else: unit_multiplier = 1.0

            buy_amt = (operating_seed / 8) * unit_multiplier
            buy_qty = int(buy_amt / target_buy_price) # ÏàòÎüâÏùÄ ÌÉÄÍ≤üÍ∞Ä Í∏∞Ï§Ä Í≥ÑÏÇ∞
            if buy_qty < 1: buy_qty = 1

            # Ïã§Ï†ú Ï≤¥Í≤∞Í∞Ä: ÌÉÄÍ≤üÍ∞ÄÎ≥¥Îã§ ÏãúÍ∞ÄÍ∞Ä ÎÇÆÍ≤å ÏãúÏûëÌñàÏúºÎ©¥ ÏãúÍ∞ÄÏ≤¥Í≤∞, ÏïÑÎãàÎ©¥ ÌÉÄÍ≤üÍ∞Ä Ï≤¥Í≤∞
            buy_price = target_buy_price
            if curr_open < target_buy_price: buy_price = curr_open

            buy_cost = buy_qty * buy_price

            if cash >= buy_cost:
                cash -= buy_cost
                
                # [Ï§ëÏöî] ÏßÑÏûÖ ÏãúÏ†êÏóê Î™©ÌëúÍ∞ÄÏôÄ ÎßåÍ∏∞Î•º ÌôïÏ†ïÌïòÏó¨ Ï†ÄÏû•
                new_pos = {
                    'buy_date': current_date,
                    'buy_price': buy_price,
                    'qty': buy_qty,
                    'target_price': buy_price * (1 + p["sell"]), # ÏùµÏ†àÍ∞Ä Í≥†Ï†ï
                    'moc_limit_days': p["moc"], # ÎßåÍ∏∞ÏùºÏàò Í≥†Ï†ï
                    'tier': current_tier_index,
                    'mode': mode
                }
                positions.append(new_pos)

        # --- [4] ÏûêÏÇ∞ ÌèâÍ∞Ä ---
        equity_val = sum([p['qty'] * curr_close for p in positions])
        total_asset = cash + equity_val
        
        history.append({'Date': current_date, 'Total': total_asset, 'Mode': mode, 'Active_Tiers': len(positions)})
        
    return pd.DataFrame(history)

