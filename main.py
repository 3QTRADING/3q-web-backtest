def get_snd_mode(target_date):
    sorted_keys = sorted(SND_DB.keys(), reverse=True)
    t_str = target_date.strftime("%y.%m.%d")
    for k in sorted_keys:
        if k <= t_str: return SND_DB[k]
    return "N"

